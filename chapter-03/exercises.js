// use recursion to find the even numbers
function isEven(n) {
  // base 
  if(n === 0) return true; // remember 0 is true
  if(n === 1) return false; // 1 is an off number



  // recursion
  // what do we want the test to do if false
  if(n < 0){ // but if n is less that 0 - which would be a negative number 
    return isEven(-n); 
  }
  return isEven(n - 2); // subtracting 2 can help us determine if its even of old due to the 1 and 0
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  // loop
  let count = 0;
  for(var i = 0; i < string.length; i++){
    if(string[i] === char){
      count += 1;
    }
  }
  return count;
}

